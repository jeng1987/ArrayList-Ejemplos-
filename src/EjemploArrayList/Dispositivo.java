public class Dispositivo {
      private int id_dis;
        private String nom_dis;
        private String tipo_dis;
        private String est_dis;

        public int getId_dis() {
            return id_dis;
        }

        public String getNom_dis() {
            return nom_dis;
        }

        public String getTipo_dis() {
            return tipo_dis;
        }

        public String getEst_dis() {
            return est_dis;
        }

        public void setId_dis(int id_dis) {
            this.id_dis = id_dis;
        }

        public void setNom_dis(String nom_dis) {
            this.nom_dis = nom_dis;
        }

        public void setTipo_dis(String tipo_dis) {
            this.tipo_dis = tipo_dis;
        }

        public void setEst_dis(String est_dis) {
            this.est_dis = est_dis;
        }
        
    
}
